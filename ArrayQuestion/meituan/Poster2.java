package algorithm.ArrayQuestion.meituan;

public class Poster2 {
    private Character name;
    private int start;
    private int end;

    public Poster2(Character name, int start, int end) {
        this.name = name;
        this.start = start;
        this.end = end;
    }

    public Character getName() {
        return name;
    }

    public void setName(Character name) {
        this.name = name;
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
}

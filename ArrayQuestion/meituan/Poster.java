package algorithm.ArrayQuestion.meituan;

public class Poster {
    private int start;
    private int end;

    public Poster(int start, int end) {
        this.start = start;
        this.end = end;
    }

    public Poster() {
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
}
